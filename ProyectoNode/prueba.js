/**
  @description dado un nombre y un apellido como parametros devuelve los dos encadenados
  @param {nombre} nombre es el nombre de la persona
  @param {apellido} apellido es el apellido de la persona
  @return {nombrecompleto} el nombre encadenado   
 **/
function pruebaNombres(nombre, apellido) {
	return nombre + " " + apellido;
}

